package com.adaptavist.tm4j.cucumberExample.calculator;

public class Calculator {
    public long sum(long n1, long n2) {
        return n1 + n2;
    }

    public long subtract(long n1, long n2) {
        return n1 - n2;
    }

    public long multiply(long n1, long n2) {
        return n1 * n2;
    }

    public long divide(long n1, long n2) {
        return n1 / n2;
    }
}
